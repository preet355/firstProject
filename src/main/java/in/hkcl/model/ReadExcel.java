package in.hkcl.model;

import java.io.Serializable;
import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="exceldata")
public class ReadExcel implements Serializable{
	
	
	private static final long serialVersionUID = 1L;

	@Id
	@Column(name="SrNo",unique=true, nullable=false)
	@GeneratedValue(strategy=GenerationType.IDENTITY)
	private Integer srNo;
	
	@Column(name="advtNo")
	private Date advtNo;

	@Column(name="catNo")
	private Integer catNo;
	
	@Column(name="department")
	private String departmentName;
	
	@Column(name="nameOfPost")
	private String nameOfPost;
	

	@Column(name="noOfPost")
	private Integer noOfPost;
	

	@Column(name="dateOfPost")
	private Date dateOfPost;
	

	@Column(name="totalNoOfAdmitCard")
	private Integer totalNoOfAdmitCard;
	
	
	@Column(name="resultOfPost")
	private Date resultOfPost;
	

	@Column(name="writtenTest")
	private Date writtenTest;
	

	@Column(name="answerKeyUploaded")
	private Date answerKeyUploaded;
	
	
	@Column(name="objection")
	private Date objection;
	

	@Column(name="revisedAnswerKey")
	private String revisedAnswerKey;
	

	@Column(name="writtenResult")
	private Date writtenResult;
	

	@Column(name="dateOfPmtHeightChest")
	private Date dateOfPmtHeightChest;
	

	@Column(name="resultOfPmtheightChest")
	private Date resultOfPmtheightChest;
	

	@Column(name="finalResultAfterInterview")
	private Date finalResultAfterInterview;
	

	@Column(name="remarks")
	private String remarks;

	
	public Integer getSrNo() {
		return srNo;
	}


	public void setSrNo(Integer srNo) {
		this.srNo = srNo;
	}


	


	public Date getAdvtNo() {
		return advtNo;
	}


	public void setAdvtNo(Date advtNo) {
		this.advtNo = advtNo;
	}


	public Integer getCatNo() {
		return catNo;
	}


	public void setCatNo(Integer catNo) {
		this.catNo = catNo;
	}


	public String getDepartmentName() {
		return departmentName;
	}


	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}


	public String getNameOfPost() {
		return nameOfPost;
	}


	public void setNameOfPost(String nameOfPost) {
		this.nameOfPost = nameOfPost;
	}


	


	public Integer getNoOfPost() {
		return noOfPost;
	}


	public void setNoOfPost(Integer noOfPost) {
		this.noOfPost = noOfPost;
	}


	public Date getDateOfPost() {
		return dateOfPost;
	}


	public void setDateOfPost(Date dateOfPost) {
		this.dateOfPost = dateOfPost;
	}


	

	public Integer getTotalNoOfAdmitCard() {
		return totalNoOfAdmitCard;
	}


	public void setTotalNoOfAdmitCard(Integer totalNoOfAdmitCard) {
		this.totalNoOfAdmitCard = totalNoOfAdmitCard;
	}


	public Date getResultOfPost() {
		return resultOfPost;
	}


	public void setResultOfPost(Date resultOfPost) {
		this.resultOfPost = resultOfPost;
	}


	public Date getWrittenTest() {
		return writtenTest;
	}


	public void setWrittenTest(Date writtenTest) {
		this.writtenTest = writtenTest;
	}


	public Date getAnswerKeyUploaded() {
		return answerKeyUploaded;
	}


	public void setAnswerKeyUploaded(Date answerKeyUploaded) {
		this.answerKeyUploaded = answerKeyUploaded;
	}


	public Date getObjection() {
		return objection;
	}


	public void setObjection(Date objection) {
		this.objection = objection;
	}


	public String getRevisedAnswerKey() {
		return revisedAnswerKey;
	}


	public void setRevisedAnswerKey(String revisedAnswerKey) {
		this.revisedAnswerKey = revisedAnswerKey;
	}


	public Date getWrittenResult() {
		return writtenResult;
	}


	public void setWrittenResult(Date writtenResult) {
		this.writtenResult = writtenResult;
	}


	public Date getDateOfPmtHeightChest() {
		return dateOfPmtHeightChest;
	}


	public void setDateOfPmtHeightChest(Date dateOfPmtHeightChest) {
		this.dateOfPmtHeightChest = dateOfPmtHeightChest;
	}


	public Date getResultOfPmtheightChest() {
		return resultOfPmtheightChest;
	}


	public void setResultOfPmtheightChest(Date resultOfPmtheightChest) {
		this.resultOfPmtheightChest = resultOfPmtheightChest;
	}


	public Date getFinalResultAfterInterview() {
		return finalResultAfterInterview;
	}


	public void setFinalResultAfterInterview(Date finalResultAfterInterview) {
		this.finalResultAfterInterview = finalResultAfterInterview;
	}


	public String getRemarks() {
		return remarks;
	}


	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}


	
	
}
